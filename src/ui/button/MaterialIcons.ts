    public replace(str: string): string {
        let result = '';
        for (let i = 0; i < str.length; i++) {
            if (str.charAt(i) == '_') {
                result += '-';
            } else {
                result += str.charAt(i);
            }
        }
        return result;
    }

    public getIcon(): HTMLElement {
        let i = Util.createI();
        i.className = 'material-icons';
        i.textContent = this.getStyle();
        return i;
    }

    private static materialIconsMap: Map<string, MaterialIcons>;

    public static get(key: string): MaterialIcons {
        if (this.materialIconsMap == null) {
            this.materialIconsMap = new Map<string, MaterialIcons>();
            let keys = Object.keys(this);
            let values = Object.values(this);
            for (let i = 0; i < keys.length; i++) {
                let key = keys[i];
                let val: MaterialIcons = values[i];
                if (!(val instanceof MaterialIcons))
                    continue;
                if (val.st == null)
                    val.st = key;
                this.materialIconsMap.set(keys[i], values[i]);
            }
        }
        return this.materialIconsMap.get(key);
    }

